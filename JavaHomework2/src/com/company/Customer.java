package com.company;

public class Customer {
    String customerName;
    String customerSurname;
    String phone;
    String mail;

    public Customer(String customerName, String customerSurname, String phone, String mail) {
        this.customerName = customerName;
        this.customerSurname = customerSurname;
        this.phone = phone;
        this.mail = mail;
    }
}
